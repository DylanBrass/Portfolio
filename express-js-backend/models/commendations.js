class Commendations{

    id;

    issuer_name;

    message;

    date;

    relationshipToMe;


    constructor(id, issuer_name, message, date, relationshipToMe){
        this.id = id;
        this.issuer_name = issuer_name;
        this.message = message;
        this.date = date;
        this.relationshipToMe = relationshipToMe;
    }



}